public class Input {
    public int c;
    public int r;
    public int s;
    public int[] serpenti;
    public int[][] rowMatrix;

    public Input(int c, int r, int s, int[] serpenti, int[][] rowMatrix) {
        this.c = c;
        this.r = r;
        this.s = s;
        this.serpenti = serpenti;
        this.rowMatrix = rowMatrix;
    }

}
